import type { NextApiRequest, NextApiResponse } from 'next'

interface ResearchResponse {
  response: string
  error?: string
}

export default async function handler(
  req: NextApiRequest,
  res: NextApiResponse<ResearchResponse>
) {
  if (req.method !== 'POST') {
    return res.status(405).json({ 
      response: '',
      error: 'Method not allowed' 
    })
  }

  try {
    const { query, model } = req.body

    if (!query) {
      return res.status(400).json({
        response: '',
        error: 'Query is required'
      })
    }

    // Call Strands Research Agent via Lambda
    try {
      const lambdaResponse = await fetch('https://kb3k29pee8.execute-api.us-east-1.amazonaws.com/prod/invoke-lambda', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          inputText: query,
          specialist: 'RESEARCH_ASSISTANT',
          model: model
        })
      })

      if (lambdaResponse.ok) {
        const data = await lambdaResponse.json()
        return res.status(200).json({ response: data.response })
      }
    } catch (error) {
      console.log('Strands Research Agent failed, using mock response')
    }

    // Fallback to mock response
    const mockResponse = generateResearchResponse(query, model)

    res.status(200).json({
      response: mockResponse
    })

  } catch (error) {
    console.error('Research Assistant error:', error)
    res.status(500).json({
      response: '',
      error: `Research failed: ${error}`
    })
  }
}

